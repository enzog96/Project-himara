        //Restaurant
        $videos = Video::first();
        $videos->videoUrl =  $request->get('videoUrl');
        Storage::put('public/assets/', $request->file('image'));
        
        $newimage = $request->file('image')->hashName();
        $image_path = public_path('storage/assets/' . $newimage);
        Image::make($image_path)->resize(1170, 750)->save(public_path('images/'  . $newimage));

        $videos->image = $newimage;
        $videos->save();

        return redirect()->back();
    }

